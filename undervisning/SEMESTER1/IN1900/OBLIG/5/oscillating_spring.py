import numpy as np
import matplotlib.pyplot as plt

# a)
def f(t):
    k = 4 #kg*s**-2
    g = 0.15 #s**-1
    m = 9 #kg
    A = -0.3 #m
    y = (A*np.exp(-g*t)) * (np.cos(np.sqrt((k)/m)*t))
    return y

n = 101 # number of points
x = np.linspace(0, 25, n) # n points in [0, 1]
y = np.zeros(n) # n zeros (float data type)
for i in range(n):
    y[i] = f(x[i])
print(y)

# b)

def g(t):
    k = 4 # kg*s**-2
    g = 0.15 # s**-1
    m = 9 # kg
    A = -0.3 # m
    y = A*np.exp(-g*t)*np.cos(np.sqrt(k/m)*t)
    return y
t_array = np.linspace(0, 25, 101)
print(g(t_array))

# c)
y = f(x)
z = g(x)
plt.subplot(3,1,1) 
plt.plot(t_array, y, "r-", label="f(x)")
plt.xlabel("t [s]")
plt.ylabel("y [m]")
plt.legend()


plt.subplot(3,1,3)
plt.plot(t_array, z, "g-", label="g(x)")
plt.xlabel("t [s]")
plt.ylabel("y [m]")
plt.legend()
plt.show()

